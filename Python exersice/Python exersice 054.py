wavelength = int(input("Enter wavelength"))

if wavelength >= 380 and wavelength <= 450:
    print("Violet")
if wavelength >= 450 and wavelength <= 495:
    print("Blue")
if wavelength >= 495 and wavelength <= 570:
    print("Green")
if wavelength >= 570 and wavelength <= 590:
    print("Yellow")
if wavelength >= 590 and wavelength <= 620:
    print("Orange")
if wavelength >= 620 and wavelength <= 750:
    print("Red")
